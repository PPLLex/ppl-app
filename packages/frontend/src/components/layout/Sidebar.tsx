export default function Sidebar({ isOpen, onClose }: { isOpen?: boolean; onClose?: () => void }) {
  const { user, logout } = useAuth();
  const { branding } = useTheme();
  const pathname = usePathname();

  if (!user) return null;

  const navItems =
    user.role === 'ADMIN' ? adminNav : user.role === 'STAFF' ? staffNav : clientNav;

  const handleNavClick = () => {
    // Close sidebar on mobile after navigation
    if (onClose) onClose();
  };

  return (
    <>
      {/* Mobile overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/60 z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`
          fixed lg:static inset-y-0 left-0 z-50
          w-64 bg-surface border-r border-border flex flex-col
          transform transition-transform duration-200 ease-in-out
          ${isOpen ? 'translate-x-0' : '-translate-x-full'}
          lg:translate-x-0
        `}
      >
        {/* Logo / Brand */}
        <div className="p-5 border-b border-border">
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-3">
              {branding.logoData ? (
                <img src={branding.logoData} alt={branding.businessName} className="w-10 h-10 rounded-full object-cover" />
              ) : (
                <div className="w-10 h-10 rounded-full ppl-gradient flex items-center justify-center">
                  <span className="text-white text-lg font-bold">{branding.businessName.charAt(0)}</span>
                </div>
              )}
              <div>
                <h2 className="font-display font-bold text-foreground text-[11px] tracking-[0.08em] uppercase leading-tight">{branding.businessName}</h2>
                {branding.tagline && (
                  <p className="text-xs text-muted leading-tight">{branding.tagline}</p>
                )}
              </div>
            </div>
            {/* Mobile close button */}
            <button
              onClick={onClose}
              className="lg:hidden text-muted hover:text-foreground p-1"
            >
              <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
              </svg>
            </button>
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 p-3 space-y-1 overflow-y-auto">
          {navItems.map((item) => {
            // Dashboard items (role root paths) should only match exactly, not every sub-route
            const isDashboard = item.label === 'Dashboard';
            const isActive = isDashboard
              ? pathname === item.href || pathname === item.href + '/dashboard'
              : pathname === item.href || pathname.startsWith(item.href + '/');
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={handleNavClick}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all ${
                  isActive
                    ? 'bg-highlight/25 text-foreground border border-highlight/40'
                    : 'text-muted hover:text-foreground hover:bg-surface-hover'
                }`}
              >
                {item.icon}
                {item.label}
              </Link>
            );
          })}
        </nav>

        {/* User info + logout */}
        <div className="p-3 border-t border-border">
          <Link
            href={user.role === 'CLIENT' ? '/client/account' : '/profile'}
            onClick={handleNavClick}
            className="block px-3 py-2 mb-2 rounded-lg hover:bg-surface-hover transition-colors cursor-pointer"
          >
            <p className="text-sm font-medium text-foreground truncate">{user.fullName}</p>
            <p className="text-xs text-muted truncate">{user.email}</p>
            {user.locations && user.locations.length > 0 ? (
              <div className="mt-0.5 space-y-0.5">
                {user.locations.map((loc) => (
                  <p key={loc.id} className="text-xs text-primary-text truncate">{loc.name}</p>
                ))}
              </div>
            ) : user.homeLocation ? (
              <p className="text-xs text-primary-text mt-0.5">{user.homeLocation.name}</p>
            ) : null}
          </Link>
          <button
            onClick={logout}
            className="flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium text-muted hover:text-danger hover:bg-danger/10 transition-all w-full"
          >
            <Icons.Logout />
            Sign Out
          </button>
        </div>
      </aside>
    </>
  );
}
